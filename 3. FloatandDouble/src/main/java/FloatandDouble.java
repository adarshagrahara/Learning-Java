
public class FloatandDouble {

	public static void main(String[] args) {
		
		Float myminFloadValue = Float.MIN_VALUE;
		Float mymaxFloadValue = Float.MAX_VALUE;
		
		System.out.println("Float minimum value is " + myminFloadValue);
		System.out.println("Float maximum value is " + mymaxFloadValue);
		
		
		Double myminDoubleValue = Double.MIN_VALUE;
		Double mymaxDoubleValue = Double.MAX_VALUE;
		
		System.out.println("Double minimum value is " + myminDoubleValue);
		System.out.println("Double maximum value is " + mymaxDoubleValue);
		
		int myIntvalue = 5 / 3;
		
		float myFloatvalue = 5f / 3f;
		//you have to keep the "f" there if you want to add a decimal values to the number
		
		double myDoublevalue = 5.00 / 3.00;
		//you dont have to keep the "d" there but it is good to do to make it clear to java
		
		System.out.println("myIntvalue = " + myIntvalue);
		System.out.println("myFloatvalue = " + myFloatvalue);
		System.out.println("myDoublevalue = " + myDoublevalue);
		
		
	
	
	}
	

}
